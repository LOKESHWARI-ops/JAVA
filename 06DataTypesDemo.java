public class DataTypesDemo {
    public static void main(String[] args) {
        byte b = 100;
        short s = 10000;
        int i = 100000;
        long l = 1000000000L;
        float f = 10.5f;
        double d = 10.5;
        char c = 'A';
        boolean bool = true;
        System.out.println("byte: "+b+"\nshort: "+s+"\nint: "+i+"\nlong: "+l+"\nfloat: "+f+"\ndouble: "+d+"\nchar: "+c+"\nboolean: "+bool);
    }
}
